void	ft_putnbr_fd(int n, int fd)
{
	long int	i;
	char		c;

	i = n;
	if (i < 0)
	{
		write (fd, '-', 1);
		i = i * -1;
	}
	if (i > 9)
	{
		ft_putnbr_fd(i / 10, fd);
		ft_putnbr_fd(i % 10, fd);
	}
	else
	{
		c = i + '0';
		write (fd, &c, 1);
	}
}
